    fun normalize(str: String): String {
        val exc = listOf(
            "de",
            "da",
            "do",
            "dos",
            "das",
            "à",
            "e"
        )
        return str
            .split(" ")
            .map { p ->
                val l = p.toLowerCase()

                if (exc.contains(l)) l
                else l.capitalize()
            }
            .joinToString(separator = " ")
    }
